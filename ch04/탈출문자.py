# 저는 "나도코딩"입니다. 를 원함
# print("저는 "나도코딩"입니다.") 오류

# \" \' : 문장 내에서 그대로 사용 가능
print("저는 '나도코딩'입니다")
print('저는 "나도코딩"입니다') # 되긴 하는데 비추
print("저는 \"나도코딩\"입니다") # \특수문자는 그대로 출력해줌

# \\ : 문장 내에서 하나의 \로 사용된다
# print("C:\Users\Song Wooseok\AppData\Local\Programs") 오류발생
print("C:\\Users\\Song Wooseok\\AppData\\Local\\Programs")

# \r : 커서를 맨 앞으로 이동
print("\n")
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭
print("Red\tApple")

